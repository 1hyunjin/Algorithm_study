WITH RANKED AS (
    SELECT ID, FISH_TYPE, LENGTH,
    RANK() OVER (PARTITION BY FISH_TYPE ORDER BY LENGTH DESC) AS rnk
    FROM FISH_INFO
)
SELECT i.ID, n.FISH_NAME, i.LENGTH
FROM RANKED i JOIN FISH_NAME_INFO n ON i.FISH_TYPE = n.FISH_TYPE
WHERE rnk = 1
ORDER BY i.ID ASC;

